            </p>
            <p>
                Одним из исторических символов Японии считается Храм Фусими-Инари или Святилище из тысячи алых ворот,
                посвященное богине рисового плодородия.
                Грандиозные тоннели с порталами, предназначенными для богов, создавались, согласно верованию синтоизма.
            </p>
            <p>
                Японцы считают, что посещение Храма Фусими-Инари приносит успех и процветание. Не сомневайтесь, что
                посмотреть в Японии.
                Особенности священного комплекса в префектуре Киото оставят неизгладимое впечатление:​​‌‌​‌ ​​‌‌​​
                ​​‌​‌‌ ​‌​​​‌ ​​‌​​​ ​​‌​​‌ ​‌​​​‌ ​​‌‌​​ ​​‌​‌‌ ​‌​​‌‌ ​‌​‌​‌​ ​‌‌​‌‌​ ​‌‌‌​‌‌ ​​‌‌‌‌​​​‌​‌ ​​​‌‌‌
                ​​‌‌‌‌ ​‌​​​‌ ​​‌‌​‌ ​​‌‌​​ ​​‌​‌‌ ​‌​​​‌ ​​‌​​​ ​​‌​​‌ ​‌​​​‌ ​​‌‌​​ ​​‌​‌‌ ​‌​​‌‌ ​‌​‌​‌​ ​‌‌​‌‌​
                ​‌‌‌​‌‌ ​​‌‌‌‌
            </p>
            <p>Храмовый комплекс расположен на горе, называемой Инари, высотой 233 метра над уровнем моря, включающий в
                себя, помимо главного святилища,
                множество небольших святилищ, расположенных на дорожках, идущих вверх по горе.
                Дорога к главному святилищу имеет протяжённость 4 километра. Путь к вершине занимает приблизительно 2
                часа.​​‌‌‌‌ ​‌​​​‌ ​​‌‌​‌ ​​‌‌​​ ​​‌​‌‌ ​‌​​​‌ ​​‌​​​ ​​‌​​‌ ​‌​​​‌ ​​‌‌​​ ​​‌​‌‌ ​‌​​‌‌ ​‌​‌​‌​ ​‌‌​‌‌​
                ​‌‌‌​‌‌ ​​‌‌‌‌​​‌‌‌‌ ​‌​​​‌ ​​‌‌​‌ ​​‌‌​​ ​​‌​‌‌ ​‌​​​‌ ​​‌​​​ ​​‌​​‌ ​‌​​​‌ ​​‌‌​​ ​​‌​‌‌ ​‌​​‌‌
                ​‌​‌​‌​ ​‌‌​‌‌​ ​‌‌‌​‌‌ ​​‌‌‌‌
            </p>
            <img class="article-image" src="images/kitsune3.jpg">
            <p>
                Важной характеристикой храмового комплекса являются более 10 тыс. ритуальных врат оранжевого цвета —
                торий, установленных на всех
                дорожках храмового комплекса столь плотно, что образуют коридоры. Тории являются подношениями храму от
                богатых людей и больших корпораций.
                Тории сделаны из дерева, покрытого слоем оранжевого лака. Чёрные цоколи торий опоясаны металлом.
                На обратной стороне каждой тории записаны имя дарителя и дата.
                Обычай дарения торий для исполнения желаний или в благодарность за исполненное желание появилось в
                период Эдо (1603—1868). ​​‌‌​​ ​​‌​‌‌ ​‌​​​‌ ​​‌​​​ ​​‌​​‌ ​‌​​​‌ ​​‌‌​​ ​​‌​‌‌ ​‌​​‌‌ ​‌​‌​‌​ ​‌‌​‌‌​
                ​‌‌‌​‌‌ ​​‌‌‌‌
            </p>
            <p>
                На дорожках храмового комплекса также расположено множество скульптурных изображений священной лисицы
                кицунэ, обязательно размещаемых
                в святилищах богини Инари. Часто кицунэ украшены красными ёдарэкакэ (нагрудниками), приносимыми в знак
                почитания.
                Одним из атрибутов кицунэ является ключ (от рисового зернохранилища) и драгоценный камень, которые
                лисицы держат в своей пасти,
                но иногда — сноп риса или свиток.
                В большинстве случаев у святилищ устанавливается пара статуй кицунэ, находящихся по бокам храма, на
                алтаре или перед главным святилищем.
            </p>
