            <h1>Храм Фусими-инари</h1>
            <img class="article-image" src="images/kitsune2.jpg">
            <p>
                伏見稲荷大社 - святилище из тысячи алых ворот, посвященное богине рисового плодородия.​​‌‌​​ ​​‌​‌‌ ​‌​​​‌
                ​​‌​​​ ​​‌​​‌ ​‌​​​‌ ​​‌‌​​ ​​‌​‌‌ ​‌​​‌‌ ​‌​‌​‌​ ​‌‌​‌‌​ ​‌‌‌​‌‌ ​​‌‌‌‌​ ​​‌‌​​ ​​‌​‌‌ ​‌​​​‌ ​​‌​​​
